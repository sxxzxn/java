package chap12;

public class Generic2<K, V> {
	private K Key;
	private V value;
	private K kkk;
	
	
	public K getKey() {
		return Key;
	}
	public void setKey(K key) {
		Key = key;
	}
	public V getValue() {
		return value;
	}
	public void setValue(V value) {
		this.value = value;
	}
	public K getKkk() {
		return kkk;
	}
	public void setKkk(K kkk) {
		this.kkk = kkk;
	}
	
	


}
