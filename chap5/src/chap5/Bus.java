package chap5;

public class Bus extends Car {
	int seatCnt;
	int standCnt;
	
	public int getSeatCnt() {
		return seatCnt;
	}
	public void setSeatCnt(int seatCnt) {
		this.seatCnt = seatCnt;
	}
	public int getStandCnt() {
		return standCnt;
	}
	public void setStandCnt(int standCnt) {
		this.standCnt = standCnt;
	}
}
